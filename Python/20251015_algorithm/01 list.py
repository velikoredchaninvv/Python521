def in_my_list(lst, num):
    '''
    Функция получает на вход список и число.
    Возвращает True, если число есть в списке.
    B False, если числа в списке нет.
    '''

    i = 0
    position_list = []

    while i < len(lst):
        if lst[i] == num:
            position_list.append(i)
        i += 1

    if position_list:
        return position_list

