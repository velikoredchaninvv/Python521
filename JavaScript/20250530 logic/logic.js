// console.log('Два равно двум?' 2===2, 2=='2');

// && и || Коньюнция и Дизюнкция
// Коньюнкция - логическое И; 0*1=0
console.log('Хлеб стоит 120 рублей И помидор стоит 500 рублей', ((bread == 120) && (tomato=500)));
// И Дизюнкция 0*1=1
console.log('Хлеб стоит 120 рублей И помидор стоит 500 рублей', ((bread == 120) || (tomato=500)));