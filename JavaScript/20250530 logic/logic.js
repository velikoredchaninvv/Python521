let bread = 120; // рублей
let tomato = 430; // помидор
// Логические вычисения
// 1. Логические константы true (истина) и false (ложь)
// 2. Результатом сравнения (<,>,<=,>=) будет логическая константа
let bread_is_cheaper = bread < tomato;
console.log('Хлеб дешевле помидора: ', bread_is_cheaper);
// 3. Результатом логической операции (!, &&, ||) будет логическая константа
console.log('Хлеб не дешевле помидора? ', !bread_is_cheaper);
// Отрицание истины - это лож
// Сравнение на равенство выполняется == или ===
